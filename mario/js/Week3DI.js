// if statement
if (someCondition) {
  // code to be executed if condition is met (i.e.: someCondition is true)
}

// if else statement
if (someCondition) {
  // code to be executed if the above condition is met (i.e.: someCondition is true)
} else {
  // code to be executed if the above condition is not met (i.e.: someCondition is false)
}

// connected if statment (else if statement)
if (someCondition) {
  // code to be executed if someCondition is true
} else if (someOtherCondition) {
  // code to be executed if someCondition is false but someOtherCondition is true
} else {
  // code to executed if both someCondition and someOtherCondition are false
}

// nested if statement (if then if)
if (someCondition) {
  if (someOtherCondition) {
    // code to executed if both someCondition and someOtherCondition are true
  }
}
